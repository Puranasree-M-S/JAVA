public class Assignment {
    public static void main(String[] args) {

        int x = 10;

        x += 5;        
        x -= 3;          
        x *= 2;         
        x /= 4;          
        x %= 3; 

        System.out.println("x += 5: " + x);  
        System.out.println("x -= 3: " + x);
        System.out.println("x *= 2: " + x); 
        System.out.println("x /= 4: " + x);
        System.out.println("x %= 3: " + x);  

    }
    
}
