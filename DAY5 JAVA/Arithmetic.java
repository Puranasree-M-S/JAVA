public class Arithmetic {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        int sum = a + b;      
        int difference = b - a;
        int product = a * b;
        int quotient = b / a;
        int remainder = b % a;

        System.out.println("The Sum of a + b = " + (sum));  // Addition 
        System.out.println("The Difference of a - b = " + (difference));  // Subtraction 
        System.out.println("The Product of a * b = " + (product));  // Multiplication 
        System.out.println("The Division of a / b = " + (quotient));  // Division (quotient)
        System.out.println("The Modulus of a % b = " + (remainder));  // Modulus  (remainder)

    }

}
