public class Data_Types {
    public static void main(String[] args) {
     
    int productId = 12345;  // Product ID (int) 
    String productName = "Samsung Galaxy S23 Ultra";  // Product Name (String)
    double price = 129999.99;  // Price (double)
    boolean isAvailable = true;  // Availability (boolean)   
    float customerRating = 4.8f;  // Customer Rating (float)
    int unitsSold = 5000;  // Number of Units Sold (int)
    double discountPercentage = 10.0; // Discount Percentage (double)
    String sellerName = "SuperDeals";  // Seller Information (String)

        // Displaying the information

        System.out.println("Product Details:");
        System.out.println("ID: " + productId);
        System.out.println("Name: " + productName);
        System.out.println("Original Price: $" + price);
        System.out.println("Discount: " + discountPercentage + "%");
        System.out.println("Availability: " + isAvailable);
        System.out.println("Rating: " + customerRating);
        System.out.println("Units Sold: " + unitsSold);
        System.out.println("Seller: " + sellerName);

        }   
    
}
